// Helper to get default meal plan
export function getDefaultMealPlan(): DailyMealPlan {
  const totalCalories = defaultMeals.reduce(
    (sum, meal) =>
      sum + meal.foods.reduce((mealSum, food) => mealSum + food.calories, 0),
    0
  );
  const totalProtein = defaultMeals.reduce(
    (sum, meal) =>
      sum + meal.foods.reduce((mealSum, food) => mealSum + food.protein, 0),
    0
  );

  return {
    meals: defaultMeals,
    totalCalories,
    totalProtein,
  };
}

// Get foods available for swapping based on meal slot
export function getSwapOptionsForSlot(slot: MealSlot): Record<string, Food[]> {
  switch (slot) {
    case "breakfast":
      return {
        Proteins: foodBank.breakfastProteins,
        Carbs: foodBank.breakfastCarbs,
      };
    case "snack1":
    case "snack2":
      return {
        "Protein Options": foodBank.snackProteins,
        "Add Fats": foodBank.fats,
      };
    case "lunch":
      return {
        "Proteins (300g raw)": foodBank.lunchProteins,
        "Carbs (70g dry)": foodBank.lunchCarbs,
        "Vegetables (200g raw)": foodBank.lunchVegetables,
        "Oil (8ml)": foodBank.fats,
      };
    case "dinner":
      return {
        "Proteins (150g raw)": foodBank.dinnerProteins,
        "Vegetables (300g raw)": foodBank.dinnerVegetables,
      };
    default:
      return {};
  }
}
